// Задача 51. Найти сумму элементов главной диагонали.
// апример, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// Сумма элементов главной диагонали: 1+9+2 = 12

int [,] arr = new int[,] {{1, 4, 7, 2},
                          {5, 9, 2, 3},
                          {8, 4, 2, 4}};
int sum = 0;               

for(int i = 0; i < 3; ++i)
{
    for(int j = 0; j < 4; ++j)
    {
        if (i == j) sum += arr[i, j]; 
    }
}
Console.WriteLine(sum);